package com.mnt.core.web.action.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;


@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface ACMethod {
	String type();
	boolean accessCheck() default false;
	int accessRole() default 0;
	String resource() default "";
	
	int CK_LOGIN				= 0x0001;
	int CK_PARAM_VALIDATION 	= 0x0002;
	
	String DEFAULT_RESOURCE_DEF = "*";
	
	
	int checkLevel() default CK_LOGIN;
}
